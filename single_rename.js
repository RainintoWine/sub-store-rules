// ==========================================
// 🚀 主执行工作流
// ==========================================
function operator(proxies) {
    // 匹配所有由两个区域指示符字母构成的 Emoji 国旗
    const flagRegex = /[\uD83C][\uDDE6-\uDDFF][\uD83C][\uDDE6-\uDDFF]/g;

    return proxies.map(p => {
        // 【第一步】先用原始未处理的名字推断国家（保留对纯国旗节点的识别能力）
        let matchedRegion = null;
        for (const [standardName, regex] of Object.entries(rurekey)) {
            if (regex.test(p.name)) {
                matchedRegion = standardName;
                break; 
            }
        }

        // 【第二步】提取出标准国旗
        let finalFlag = "";
        if (matchedRegion) {
            const index = ZH.indexOf(matchedRegion);
            if (index !== -1) {
                finalFlag = FG[index] + " "; 
            }
        }

        // 【第三步】用手术刀物理剥离原名中所有的旧国旗，得到无损底板
        let pureName = p.name.replace(flagRegex, '').trim();

        // 【第四步】无损拼装
        let prefix = QUALITY_TAG ? `${QUALITY_TAG} ` : "";
        let suffix = AIRPORT_NAME ? `-${AIRPORT_NAME}` : "";

        // 终极输出赋值
        p.name = `${prefix}${finalFlag}${pureName}${suffix}`;

        return p;
    });
}
